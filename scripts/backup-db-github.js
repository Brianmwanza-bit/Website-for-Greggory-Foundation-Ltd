const { exec } = require('child_process');
const fs = require('fs');
const path = require('path');
require('dotenv').config();

// Configuration
const DB_CONFIG = {
  host: process.env.DB_HOST || 'localhost',
  user: process.env.DB_USER || 'root',
  password: process.env.DB_PASSWORD || '',
  database: process.env.DB_NAME || 'greggory_foundation_db_main',
};

const BACKUP_DIR = path.join(__dirname, '../backups');
const TIMESTAMP = new Date().toISOString().replace(/[:.]/g, '-').split('T')[0];
const BACKUP_FILE = `backup-${TIMESTAMP}.sql`;
const BACKUP_PATH = path.join(BACKUP_DIR, BACKUP_FILE);

// Ensure backup directory exists
if (!fs.existsSync(BACKUP_DIR)) {
  fs.mkdirSync(BACKUP_DIR, { recursive: true });
  console.log('Created backups directory');
}

console.log('='.repeat(60));
console.log('DATABASE BACKUP TO GITHUB');
console.log('='.repeat(60));
console.log(`Database: ${DB_CONFIG.database}`);
console.log(`Host: ${DB_CONFIG.host}`);
console.log(`Backup file: ${BACKUP_FILE}`);
console.log('='.repeat(60));

// Create database dump using mysqldump
const dumpCmd = `mysqldump --host=${DB_CONFIG.host} --user=${DB_CONFIG.user} --password=${DB_CONFIG.password} --databases ${DB_CONFIG.database} --single-transaction --quick --lock-tables=false > ${BACKUP_PATH}`;

console.log('\nCreating database backup...');
exec(dumpCmd, (error, stdout, stderr) => {
  if (error) {
    console.error('❌ Error creating backup:', error.message);
    
    // Fallback: Try alternative method using mysql2
    console.log('\n⚠️  Trying alternative backup method using Node.js...');
    createBackupWithNodeJS();
    return;
  }

  if (stderr) {
    console.warn('Warning:', stderr);
  }

  // Check if backup file was created
  if (fs.existsSync(BACKUP_PATH)) {
    const stats = fs.statSync(BACKUP_PATH);
    console.log(`✅ Backup created successfully!`);
    console.log(`📁 Location: ${BACKUP_PATH}`);
    console.log(`📊 Size: ${(stats.size / 1024).toFixed(2)} KB`);
    
    // Clean up old backups (keep last 5)
    cleanOldBackups();
    
    console.log('\n' + '='.repeat(60));
    console.log('NEXT STEPS:');
    console.log('='.repeat(60));
    console.log('1. Review the backup file if needed');
    console.log('2. Commit to GitHub:');
    console.log(`   git add ${BACKUP_FILE}`);
    console.log(`   git commit -m "Database backup ${TIMESTAMP}"`);
    console.log('   git push');
    console.log('='.repeat(60));
  } else {
    console.error('❌ Backup file was not created');
    process.exit(1);
  }
});

// Alternative backup method using Node.js
function createBackupWithNodeJS() {
  const mysql = require('mysql2/promise');
  
  mysql.createConnection({
    host: DB_CONFIG.host,
    user: DB_CONFIG.user,
    password: DB_CONFIG.password,
    database: DB_CONFIG.database
  }).then(async (connection) => {
    console.log('✅ Connected to database');
    
    // Get all tables
    const [tables] = await connection.query('SHOW TABLES');
    const tableNames = tables.map(t => Object.values(t)[0]);
    
    console.log(`Found ${tableNames.length} tables`);
    
    let sql = `-- Database Backup: ${DB_CONFIG.database}\n`;
    sql += `-- Date: ${new Date().toISOString()}\n`;
    sql += `-- Generated by backup-db-github.js\n\n`;
    sql += `CREATE DATABASE IF NOT EXISTS \`${DB_CONFIG.database}\`;\n`;
    sql += `USE \`${DB_CONFIG.database}\`;\n\n`;
    sql += `SET SQL_MODE = 'NO_AUTO_VALUE_ON_ZERO';\n`;
    sql += `SET time_zone = '+00:00';\n\n`;
    
    for (const table of tableNames) {
      console.log(`Backing up table: ${table}`);
      
      // Get CREATE TABLE statement
      const [createResult] = await connection.query(`SHOW CREATE TABLE \`${table}\``);
      sql += `-- Table structure for ${table}\n`;
      sql += `DROP TABLE IF EXISTS \`${table}\`;\n`;
      sql += createResult[0]['Create Table'] + ';\n\n';
      
      // Get table data
      const [rows] = await connection.query(`SELECT * FROM \`${table}\``);
      if (rows.length > 0) {
        sql += `-- Data for ${table}\n`;
        sql += `INSERT INTO \`${table}\` VALUES\n`;
        
        const values = rows.map(row => {
          const vals = Object.values(row).map(val => {
            if (val === null) return 'NULL';
            if (typeof val === 'string') return `'${val.replace(/'/g, "\\'")}'`;
            if (typeof val === 'object') return `'${JSON.stringify(val).replace(/'/g, "\\'")}'`;
            return val;
          });
          return `(${vals.join(', ')})`;
        });
        
        sql += values.join(',\n');
        sql += ';\n\n';
      }
    }
    
    await connection.end();
    
    // Write to file
    fs.writeFileSync(BACKUP_PATH, sql);
    const stats = fs.statSync(BACKUP_PATH);
    
    console.log(`✅ Backup created successfully using Node.js!`);
    console.log(`📁 Location: ${BACKUP_PATH}`);
    console.log(`📊 Size: ${(stats.size / 1024).toFixed(2)} KB`);
    
    cleanOldBackups();
    
    console.log('\n' + '='.repeat(60));
    console.log('NEXT STEPS:');
    console.log('='.repeat(60));
    console.log('1. Review the backup file if needed');
    console.log('2. Commit to GitHub:');
    console.log(`   git add ${BACKUP_FILE}`);
    console.log(`   git commit -m "Database backup ${TIMESTAMP}"`);
    console.log('   git push');
    console.log('='.repeat(60));
    
  }).catch(err => {
    console.error('❌ Alternative backup method also failed:', err.message);
    console.error('\nPlease ensure:');
    console.error('1. XAMPP MySQL is running');
    console.error('2. Database credentials in .env are correct');
    console.error('3. Database exists in phpMyAdmin');
    process.exit(1);
  });
}

// Clean up old backups (keep last 5)
function cleanOldBackups() {
  try {
    const files = fs.readdirSync(BACKUP_DIR)
      .filter(f => f.startsWith('backup-') && f.endsWith('.sql'))
      .map(f => ({
        name: f,
        path: path.join(BACKUP_DIR, f),
        time: fs.statSync(path.join(BACKUP_DIR, f)).mtime.getTime()
      }))
      .sort((a, b) => b.time - a.time);
    
    if (files.length > 5) {
      const toDelete = files.slice(5);
      toDelete.forEach(f => {
        fs.unlinkSync(f.path);
        console.log(`🗑️  Deleted old backup: ${f.name}`);
      });
    }
  } catch (err) {
    console.warn('Warning: Could not clean old backups:', err.message);
  }
}
